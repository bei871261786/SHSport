public class DingdanBean implements Serializable {
    private int totalMoney;
    private int totalCount;
    private int PRICE = 2;//每注金额
    private int multiple = 1;//倍数
    private String issueNo;
    private ArrayList<DingdanItemBean> dingdanItems;
    private String lotId;
    private int mJcType;

    private ArrayList<String> mChuanlianList = new ArrayList<>();//串联方式集合


    public int getPRICE() {
        return PRICE;
    }

    public int getMultiple() {
        return multiple;
    }

    public void setMultiple(int multiple) {
        this.multiple = multiple;
    }

    public int getTotalCount() {
        return totalCount;
    }

    public void setTotalCount(int totalCount) {
        this.totalCount = totalCount;
    }

    public ArrayList<String> getmChuanlianList() {
        return mChuanlianList;
    }

    public void setmChuanlianList(ArrayList<String> mChuanlianList) {
        this.mChuanlianList = mChuanlianList;
    }

    public int getmJcType() {
        return mJcType;
    }

    public void setmJcType(int mJcType) {
        this.mJcType = mJcType;
    }

    public String getLotId() {
        return lotId;
    }

    public void setLotId(String lotId) {
        this.lotId = lotId;
    }


    public int getTotalMoney() {
        return totalMoney;
    }

    public void setTotalMoney(int totalMoney) {
        this.totalMoney = totalMoney;
    }

    public String getIssueNo() {
        return issueNo;
    }

    public void setIssueNo(String issueNo) {
        this.issueNo = issueNo;
    }


    public ArrayList<DingdanItemBean> getDingdanItems() {
        return dingdanItems;
    }

    public void setDingdanItems(ArrayList<DingdanItemBean> dingdanItems) {
        this.dingdanItems = dingdanItems;
    }
}

