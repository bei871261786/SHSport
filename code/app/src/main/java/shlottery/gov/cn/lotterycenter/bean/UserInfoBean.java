public class UserInfoBean implements Serializable {

    private String secret;
    private String mobile;
    private String nickName;
    private String logoUrl;
    private String integral;//积分
    private int userType;
    private String  subscribeNum;
    private String  realName;//真实姓名
    private String  idNumber;//身份证号
    private boolean signIn;//是否签到
    private String siteRegUrl;

    public boolean isSignIn() {
        return signIn;
    }

    public void setSignIn(boolean signIn) {
        this.signIn = signIn;
    }

    public String getSiteRegUrl() {
        return siteRegUrl;
    }

    public void setSiteRegUrl(String siteRegUrl) {
        this.siteRegUrl = siteRegUrl;
    }

    public String getSubscribeNum() {
        return subscribeNum;
    }

    public void setSubscribeNum(String subscribeNum) {
        this.subscribeNum = subscribeNum;
    }

    public String getIntegral() {
        return integral;
    }

    public void setIntegral(String integral) {
        this.integral = integral;
    }

    public String getSecret() {
        return secret;
    }

    public void setSecret(String secret) {
        this.secret = secret;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getNickName() {
        return nickName;
    }

    public void setNickName(String nickName) {
        this.nickName = nickName;
    }

    public String getLogoUrl() {
        return logoUrl;
    }

    public void setLogoUrl(String logoUrl) {
        this.logoUrl = logoUrl;
    }

    public String getRealName() {
        return realName;
    }

    public void setRealName(String realName) {
        this.realName = realName;
    }

    public int getUserType() {
        return userType;
    }

    public void setUserType(int userType) {
        this.userType = userType;
    }

    public String getIdNumber() {
        return idNumber;
    }

    public void setIdNumber(String idNumber) {
        this.idNumber = idNumber;
    }

    @Override
    public String toString() {
        return "mobile:" + mobile + ":nickName:" + nickName + "::logoUrl:" + logoUrl + "::realName:" + realName + "::integral:" + integral + "::userType:" + userType;
    }
}
